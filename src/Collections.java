public class Collections {
    // ArrayList, HashSet and HashMap, and other data structures, belong to the Collections Framework, from the java.utils package.
    // These classes implements Interfaces, with their own methods, you cannot create Objects from Interfaces,
    // so you use these classes to implement then.

    // Core Interfaces in the Collections Framework
    // Interfaces  - Classes                         - Description
    // List        - ArrayList, LinkedList           - Ordered collection that allows duplicates
    // Set         - HashSet, TreeSet, LinkedHashSet - Collection of unique elements
    // Map         - HashMap, TreeMap, LinkedHashMap - Stores key-value pairs with unique keys
}